PROGRAM BINOMIAL
INTEGER N, R
REAL Y
PRINT*, 'ENTER VALUE FOR N'
READ(*,*) N
PRINT*, 'ENTER VALUE FOR R'
READ(*,*) R

Y = BIN(N,R)
WRITE(*,*) Y

CONTAINS 
FUNCTION BIN(N,R)
INTEGER N, R,P
REAL BIN,NFACT,NRFACT,RFACT,A,B
NFACT=1
NRFACT=1
RFACT=1
DO I = 1,N 
  NFACT = NFACT*I
  END DO
 
P =N-R
DO I = 1,P
  NRFACT = NRFACT* I
  END DO
  
DO I = 1,R
  RFACT = RFACT*I
  END DO 
  

 A=NFACT/NRFACT
 B = RFACT
 BIN = A/B
  END FUNCTION
  
  END PROGRAM